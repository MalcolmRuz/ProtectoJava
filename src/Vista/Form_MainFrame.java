    public Form_MainFrame() {
        initComponents();

        setTitle("Batman vs Superman");
        setSize(900, 600);
        setLocationRelativeTo(null);
        setDefaultCloseOperation(EXIT_ON_CLOSE);

        // --- Cargar el GIF ---
        ImageIcon gifFondo = new ImageIcon(getClass().getResource("/Imagenes/GiftPelea.gif"));

        // --- Crear un JPanel personalizado que dibuje el GIF ---
        JPanel panelFondo = new JPanel() {
            @Override
            protected void paintComponent(Graphics g) {
                super.paintComponent(g);
                Image img = gifFondo.getImage();
                g.drawImage(img, 0, 0, getWidth(), getHeight(), this);
            }
        };
        panelFondo.setLayout(null); // para colocar elementos libremente
        setContentPane(panelFondo); // usarlo como fondo principal

        // --- Texto encima del GIF ---
        JLabel texto = new JLabel("Â¡La pelea definitiva!");
        texto.setFont(new Font("Arial", Font.BOLD, 28));
        texto.setForeground(Color.BLACK);
        texto.setBounds(50, 30, 400, 50);
        panelFondo.add(texto);

        setVisible(true);
    }

